package json;

public class Food {
	public String name;
    public String spicy;
    public String size;
    public String time;
    public int price;

    public Food(String name, String spicy, String size, String time, int price) {
        super();
        this.name = name;
        this.spicy = spicy;
        this.size = size;
        this.time = time;
        this.price = price;
    }
}
